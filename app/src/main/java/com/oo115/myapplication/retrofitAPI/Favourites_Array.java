package com.oo115.myapplication.retrofitAPI;

import com.google.gson.annotations.Expose;
import com.google.gson.annotations.SerializedName;

public class Favourites_Array {
    @SerializedName("fav_id")
    @Expose
    private String id;

    @SerializedName("exercise_id")
    @Expose
    private String ex_id;

    @SerializedName("exercise_name")
    @Expose
    private String name;

    @SerializedName("exercise_description")
    @Expose
    private String description;

    @SerializedName("exercise_muscle_group")
    @Expose
    private String muscle_group;

    @SerializedName("exercise_image")
    @Expose
    private String image;

    public String getEx_id() {
        return ex_id;
    }

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getDescription() {
        return description;
    }

    public String getMuscle_group() {
        return muscle_group;
    }

    public String getImage() {
        return image;
    }

}
