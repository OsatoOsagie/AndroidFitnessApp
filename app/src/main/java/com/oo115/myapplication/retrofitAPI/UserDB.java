    public String getFat() {
        return fat;
    }

    public void setFat(String fat) {
        this.fat = fat;
    }

    public String getWater() {
        return water;
    }

    public void setWater(String water) {
        this.water = water;
    }

    public String getWeekly_target() {
        return weekly_target;
    }

    public String getWeekly_actiityLevel() {
        return weekly_actiityLevel;
    }

